$token = '';
$chars = str_split("abcdefghijklmnopqrstuvwxyz0123456789");
for ($i = 0; $i < 16; $i++) {
    $token .= $chars[array_rand($chars)];
}

 $config = Mage::getModel('core/config');
$config->saveConfig('sooqr_connect/generate/token', $token, 'default', 0);